package com.meli.eval.model;

/**
 * Nucleobases, also known as nitrogenous bases or often simply bases,
 * are nitrogen-containing biological compounds that form nucleosides,
 * which in turn are components of nucleotides, with all of these monomers
 * constituting the basic building blocks of nucleic acids.
 *
 * See https://en.wikipedia.org/wiki/Nucleobase for more information.
 */
public enum NitrogenNucleobase {
    /**
     * Adenosine
     */
    A,

    /**
     * Thymidine
     */
    T,

    /**
     * Cytosine
     */
    C,

    /**
     * Guanine
     */
    G
}
